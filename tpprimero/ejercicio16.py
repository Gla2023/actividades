print("Calculo del área de un triángulo")
base_del_triángulo=float(input("Ingrese la medida de la base del triángulo:"))
altura_del_triángulo=float(input("Ingrese la medida de la altura del triángulo:"))
area_del_triángulo=base_del_triángulo*altura_del_triángulo*1/2
print("El área del triángulo es:", area_del_triángulo)